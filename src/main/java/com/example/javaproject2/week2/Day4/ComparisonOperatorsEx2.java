package com.example.javaproject2.week2.Day4;

public class ComparisonOperatorsEx2 {
    public static void main(String[] args) {
        int aVal = 10;
        int bVal = 20;
        System.out.printf("%d == %d --> %bVal\n", aVal, bVal, aVal == bVal);
        System.out.printf("%d != %d --> %bVal\n", aVal, bVal, aVal != bVal);
        System.out.printf("%d < %d --> %bVal\n", aVal, bVal, aVal < bVal);
        System.out.printf("%d <= %d --> %bVal\n", aVal, bVal, aVal <= bVal);
        System.out.printf("%d > %d --> %bVal\n", aVal, bVal, aVal > bVal);
        System.out.printf("%d >= %d --> %bVal\n", aVal, bVal, aVal >= bVal);
    }
}